void main()
{
 
  int num1 = 10;
  int num2 = 5;

  //greater than >
  print(num1 > num2);

  //less than < 
  print(num1 < num2);
 
  // greater than or equal
  print(num1 >= num2);

  // Less than or equal to
  print(num1 <= num2);

  // equal to ==
  print(num1 == num2);

  //not equal !=
  print(num1 != num2);
}
