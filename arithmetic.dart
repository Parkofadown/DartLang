void main()
{
  int num1 = 10;
  int num2 = 5;  
 // add
  print(num1 + num2);
 // subtract
  print(num1 - num2);
 // multiply
  print(num1 * num2);
 // division
  print(num1 / num2);
 // unary minus
  print(-num1);
 // modulus
  print(num1 % num2);
 // increment 
  num1++;
  // decrement
  num1--;
}
